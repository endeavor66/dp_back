package com.example.supplychain.vo;

public class MaterialNeedVO {
    String id;
    String name;

    public MaterialNeedVO(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
